SI_TABLE_RECORD g_SiTblRec;

void InitSiTblRec()
{
    U32BIT data_len = 0;
    SI_SECTION_RECORD *si_sec_rec_1 = NULL;
    SI_SECTION_RECORD *si_sec_rec_0 = NULL;

    g_SiTblRec.path = 0;
    g_SiTblRec.pid  = 0x776;

    g_SiTblRec.tid      = 0x91;
    g_SiTblRec.xtid     = 0x01;

    g_SiTblRec.version  = 0x01;
    g_SiTblRec.num_sect = 2;

    data_len = sizeof (sgt_sec_1);
    si_sec_rec_1 = STB_GetMemory(sizeof(SI_SECTION_RECORD) + data_len);
    memcpy((void *)&(si_sec_rec_1->data_start), (void *)&sgt_sec_1[0], (size_t)data_len);

    si_sec_rec_1->sect_num  = 1;
    si_sec_rec_1->data_len  = data_len;
    si_sec_rec_1->next      = NULL;

    CERT_LOG_DEBG(TAG, "sec_rec_1 len:%d \n", data_len);

    data_len = sizeof (sgt_sec_0);
    si_sec_rec_0 = STB_GetMemory(sizeof(SI_SECTION_RECORD) + data_len);
    memcpy((void *)&(si_sec_rec_0->data_start), (void *)&sgt_sec_0[0], (size_t)data_len);

    si_sec_rec_0->sect_num  = 0;
    si_sec_rec_0->data_len  = data_len;
    si_sec_rec_0->next      = si_sec_rec_1;

     CERT_LOG_DEBG(TAG, "sec_rec_0 len:%d \n", data_len);

    g_SiTblRec.section_list = si_sec_rec_0;
}

void DeinitSiTblRec()
{
    SI_SECTION_RECORD *next = NULL;
    SI_SECTION_RECORD *cur  = g_SiTblRec.section_list;

    while (NULL != cur)
    {
        next = cur->next;

        STB_FreeMemory(cur);

        cur = next;
    }
}

void Test_Parse_SGT()
{
    SI_SGT_TABLE *sgt_tbl_ptr = NULL;

    InitSiTblRec();

    sgt_tbl_ptr = cert_STB_SIParseSgtTable(&g_SiTblRec);

    cert_Show_SgtTable(sgt_tbl_ptr);

    cert_Free_SgtTable(sgt_tbl_ptr);

    DeinitSiTblRec();
}



